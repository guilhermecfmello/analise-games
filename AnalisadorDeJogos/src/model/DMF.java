package model;

public class DMF {
	private Integer site_sold;
	private String name_dev;
	private Integer count;
	
	
	public Integer getSite_sold() {
		return site_sold;
	}
	public void setSite_sold(Integer site_sold) {
		this.site_sold = site_sold;
	}
	public String getName_dev() {
		return name_dev;
	}
	public void setName_dev(String name_dev) {
		this.name_dev = name_dev;
	}
	public Integer getCount() {
		return count;
	}
	public void setCount(Integer count) {
		this.count = count;
	}
	
	
}
